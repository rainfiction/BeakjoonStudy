#include <iostream>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;

    while(n--){
        cout << " @@@   @@@ " << '\n';
        cout << "@   @ @   @" << '\n';
        cout << "@    @    @" << '\n';
        cout << "@         @" << '\n';
        cout << " @       @ " << '\n';
        cout << "  @     @  " << '\n';
        cout << "   @   @   " << '\n';
        cout << "    @ @    " << '\n';
        cout << "     @     " << '\n';
    }

    return 0;
}